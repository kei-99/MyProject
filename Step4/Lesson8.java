public class Lesson8 {
    public static void main(String[] args) {
        // 1から10まで順に表示
        System.out.println("1から10までの表示:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // 10から1まで逆順に表示
        System.out.println("10から1までの逆順表示:");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
